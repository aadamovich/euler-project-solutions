// Starting in the top left corner of a 2x2 grid, there are 6 routes (without backtracking) to the bottom right corner.
//
// How many routes are there through a 20x20 grid?
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

BigInteger n = 20

def fact(n) {
 BigInteger f = 1
 for(i = 1; i <=n; i++) {
   f *= i
 }
 f
}

def fn = fact(n)
println fact(2*n)/(fn*fn)